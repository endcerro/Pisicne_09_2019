char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	unsigned int dest_size;
	unsigned int i;

	i = 0;
	dest_size = 0;
	while (dest[dest_size])
		dest_size++;
	while (src[i] && i < nb)
	{
		dest[dest_size] = src[i];
		dest_size++;
		i++;
	}
	dest[dest_size] = '\0';
	return (dest);
}
