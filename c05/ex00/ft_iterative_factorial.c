int	ft_iterative_factorial(int nb)
{
	int i;
	int nbo;

	if (nb < 0)
		return (0);
	nbo = 1;
	i = 1;
	while (i <= nb)
	{
		nbo = nbo * i;
		i++;
	}
	return (nbo);
}
