#include <unistd.h>

void	ft_putstr(const char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
}

int		main(int argc, char const *argv[])
{
	int i;

	i = 1;
	while (i < argc)
		ft_putstr(argv[i++]);
	return (0);
}
