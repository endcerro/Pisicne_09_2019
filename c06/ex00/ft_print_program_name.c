#include <unistd.h>

void	ft_putstr(const char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
}

int		main(int argc, char const *argv[])
{
	if (argc > 0)
		ft_putstr(argv[0]);
	return (0);
}
